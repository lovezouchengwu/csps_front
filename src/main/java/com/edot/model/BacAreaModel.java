package com.edot.model;

import javax.persistence.Entity;

/**
 * 地区
 * @author tony
 *
 */
@Entity(name = "bac_area")
public class BacAreaModel extends BaseModel {
	
	// 地区名称
	private String name;
	// 上级地区
	private BacAreaModel parent;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public BacAreaModel getParent() {
		return parent;
	}
	public void setParent(BacAreaModel parent) {
		this.parent = parent;
	}

}
